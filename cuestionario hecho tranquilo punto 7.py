

n=int(input("Ingrese el valor tope de la sumas"))

suma=0
i=1


while suma<n:
    suma= suma + 5*i
    i=i+1
print("el importe se pasa cuando llega a", suma)
print("necesita iterar la cantidad de", i-1, "veces")


